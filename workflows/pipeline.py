from crewai import Task


def build_tasks(user_input, researcher, writer, auditor):
    """
    构建三阶段任务流：
    1. 先检索和归纳依据
    2. 再组织成面向用户的答复
    3. 最后做口径审校
    """

    task_research = Task(
        description=f"""
用户问题：{user_input}

请完成以下工作：
1. 明确用户关注的核心政策问题。
2. 检索知识库中的相关政策原文、政策解读和背景资料。
3. 需要特别注意：知识库中的单篇文件，可能同时包含政策原文和解读，也可能只有政策原文而没有解读。
4. 在阅读时区分哪些内容属于政策原文要求，哪些内容属于解读性表述、背景说明或经验性总结。
5. 提炼与问题直接相关的结论依据，包括：
   - 政策主题
   - 适用对象
   - 适用条件
   - 时间范围
   - 执行要求
   - 明显限制或例外情况
6. 如果知识库资料不足以支持明确判断，必须明确写出信息缺口与不确定项。

输出要求：
- 全程使用中文。
- 以“结构化研究结果”形式输出。
- 不直接给最终建议，只做事实归纳和依据整理。
""",
        expected_output=(
            "一份中文的结构化研究结果，至少包含：核心问题、政策依据、"
            "适用对象、适用条件、时间范围、执行要求、不确定项。"
        ),
        agent=researcher,
    )

    task_write = Task(
        description="""
请基于研究结果，生成一份面向用户的中文答复，用于政策问答、政策总结解读和行动建议。

写作要求：
1. 先给结论，结论必须建立在现有政策依据上。
2. 再说明政策依据，并尽量交代适用条件、范围和限制。
3. 如果引用的是带有解读性质的内容，要在表达上与政策原文要求区分开，不得混写。
4. 如果用户问题涉及如何处理、如何申请、如何准备材料、如何规避风险等，请在政策依据支持的前提下给出建议动作。
5. 对于无法从知识库中明确支持的内容，要直接说明“现有资料不足以支持明确结论”或同等含义，不得自行补充。
6. 语言要清晰、克制、稳妥，避免夸大和武断表述。

默认按以下结构组织：
- 结论
- 政策依据
- 适用条件 / 限制
- 建议动作
- 不确定事项
""",
        expected_output=(
            "一份完整的中文政策答复，包含结论、政策依据、适用条件或限制、"
            "建议动作以及不确定事项。"
        ),
        agent=writer,
        context=[task_research],
    )

    task_audit = Task(
        description="""
请对答复进行政策口径审校，并输出最终版本。

重点检查：
1. 结论是否有明确政策依据支撑。
2. 是否混淆政策原文要求与解读性表述。
3. 当单篇文件同时包含原文和解读时，最终答复是否错误地把解读部分写成政策硬性要求。
4. 是否遗漏适用对象、适用条件、时间要求或限制条件。
5. 建议动作是否超出知识库支持范围。
6. 若资料不足，是否已经明确提示不确定项，而不是给出过强判断。

处理要求：
- 如有问题，直接修正。
- 优先保留稳妥、审慎、可核实的表达。
- 最终输出面向用户，使用中文。
""",
        expected_output=(
            "一份经过审校后的最终中文答复，口径稳妥，依据清晰，"
            "明确区分结论、依据、条件、建议和不确定事项。"
        ),
        agent=auditor,
        context=[task_write],
    )

    return [task_research, task_write, task_audit]
